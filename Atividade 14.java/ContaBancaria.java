public class ContaBancaria {
    private Double saldo;
    private Double valor;

    public ContaBancaria (Double saldo, Double valor){
        this.saldo = saldo;
        this.valor = valor;
    }

    public Double get_saldo (){
        return saldo;
    }

    public Double get_valor (){
        return valor;
    }

    public Double depositar (){
        return valor;
    }

    public Double sacar (){
        return valor;
    }
}
